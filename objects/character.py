class Character:
    def __init__(self, name, picture, hp, mp, defense, dexterity, strength, intelligence, faith, charisma):
        self.name = name
        self.picture = picture
        self.hp = hp
        self.mp = mp
        self.defense = defense
        self.dexterity = dexterity
        self.strength = strength
        self.intelligence = intelligence
        self.faith = faith
        self.charisma = charisma

